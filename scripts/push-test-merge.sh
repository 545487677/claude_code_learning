#!/bin/bash
# Push → Test → Merge 自动化脚本
# 基于 .claude/skills/push-test-merge.md

set -e  # 遇到错误立即退出

echo "╔═══════════════════════════════════════╗"
echo "║  Push → Test → Merge 自动化流程       ║"
echo "╚═══════════════════════════════════════╝"
echo ""

# 步骤1: 检查当前状态
echo "📋 步骤1/7: 检查当前状态..."
CURRENT_BRANCH=$(git branch --show-current)
echo "   当前分支: $CURRENT_BRANCH"

if [ "$CURRENT_BRANCH" = "main" ] || [ "$CURRENT_BRANCH" = "master" ]; then
    echo "❌ 错误: 不能在 main/master 分支上执行此操作"
    exit 1
fi

git status --short
echo ""

# 步骤2: 提交未提交的更改（如果有）
if [[ -n $(git status --short) ]]; then
    echo "📝 步骤2/7: 提交更改..."
    git add .
    git commit -m "自动提交：由 push-test-merge 脚本创建 ($(date))"
    echo "✅ 提交完成"
else
    echo "✅ 步骤2/7: 没有需要提交的更改"
fi
echo ""

# 步骤3: 推送到远程
echo "🚀 步骤3/7: 推送到远程..."
git push origin "$CURRENT_BRANCH" || git push -u origin "$CURRENT_BRANCH"
echo "✅ 推送完成"
echo ""

# 步骤4: 运行测试（如果存在）
echo "🧪 步骤4/7: 运行测试..."
if [ -f "package.json" ]; then
    if grep -q '"test"' package.json; then
        echo "   检测到 Node.js 测试"
        npm test || { echo "❌ 测试失败"; exit 1; }
    fi
elif [ -f "pytest.ini" ] || [ -f "setup.py" ]; then
    echo "   检测到 Python 测试"
    pytest || { echo "❌ 测试失败"; exit 1; }
elif [ -f "go.mod" ]; then
    echo "   检测到 Go 测试"
    go test ./... || { echo "❌ 测试失败"; exit 1; }
else
    echo "   ⏭️  没有检测到测试"
fi
echo "✅ 测试通过"
echo ""

# 步骤5: 创建 PR
echo "🔀 步骤5/7: 创建 Pull Request..."
if command -v gh &> /dev/null; then
    # 使用 GitHub CLI
    PR_URL=$(gh pr create \
        --title "Auto PR: $CURRENT_BRANCH" \
        --body "## 自动创建的PR

由 push-test-merge 脚本自动生成

### 变更内容
$(git log origin/main..HEAD --oneline)

### 测试状态
✅ 所有测试通过

🤖 Generated by automated script" \
        --base main \
        2>&1)

    PR_NUMBER=$(echo "$PR_URL" | grep -oP 'pull/\K\d+' || echo "")
    echo "✅ PR 已创建: $PR_URL"
else
    echo "❌ 需要安装 gh CLI: https://cli.github.com/"
    exit 1
fi
echo ""

# 步骤6: 询问是否合并
echo "⏸️  步骤6/7: 合并 Pull Request"
read -p "是否立即合并 PR? (yes/no): " MERGE_CONFIRM
if [ "$MERGE_CONFIRM" = "yes" ] || [ "$MERGE_CONFIRM" = "y" ]; then
    gh pr merge "$PR_NUMBER" --squash --delete-branch
    echo "✅ PR 已合并并删除远程分支"
else
    echo "⏭️  跳过合并，PR 保持打开状态"
    exit 0
fi
echo ""

# 步骤7: 清理本地分支
echo "🧹 步骤7/7: 清理本地分支..."
git checkout main
git pull origin main
git branch -D "$CURRENT_BRANCH"
echo "✅ 本地分支已删除"
echo ""

# 生成报告
echo "╔═══════════════════════════════════════╗"
echo "║  执行报告                             ║"
echo "╚═══════════════════════════════════════╝"
echo ""
echo "✅ 所有步骤执行成功！"
echo "   分支: $CURRENT_BRANCH → main"
echo "   PR: $PR_URL"
echo ""
echo "🎉 完成！"
